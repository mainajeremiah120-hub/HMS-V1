import User from "./auth.model.js";
import crypto from 'crypto';

/**
 * Helper: The "Random Password Machine"
 * Exported so staff.controller.js can use it.
 */
export const generateRandomPassword = (length = 10) => {
  return crypto.randomBytes(length).toString('hex').slice(0, length);
};

// @desc    Register a new staff member
// @route   POST /api/auth/register
export const registerUser = async (req, res) => {
  try {
    const { name, email, role, staffId } = req.body;

    if (!name || !email) {
      return res.status(400).json({ success: false, message: "Please provide name and email" });
    }

    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(409).json({ success: false, message: "User already exists" });
    }

    const autoGeneratedPassword = generateRandomPassword(10);

    const user = await User.create({
      name,
      email,
      password: autoGeneratedPassword, 
      role: role || "doctor",
      staffId,
    });

    const token = user.getJWT();

    res.status(201).json({
      success: true,
      token,
      tempPassword: autoGeneratedPassword,
      user: { id: user._id, name: user.name, email: user.email, role: user.role },
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// @desc    Login staff member
// @route   POST /api/auth/login
export const loginUser = async (req, res) => {
  try {
    const { email, password } = req.body;

    if (!email || !password) {
      return res.status(400).json({ success: false, message: "Provide email and password" });
    }

    const user = await User.findOne({ email }).select("+password");

    if (!user || !user.isActive) {
      return res.status(401).json({ success: false, message: "Invalid credentials or account disabled" });
    }

    const isPasswordValid = await user.matchPassword(password);
    if (!isPasswordValid) {
      return res.status(401).json({ success: false, message: "Invalid credentials" });
    }

    const token = user.getJWT();
    res.json({
      success: true,
      token,
      user: { id: user._id, name: user.name, email: user.email, role: user.role, staffId: user.staffId },
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// @desc    Get currently logged in user
// @route   GET /api/auth/me
export const getCurrentUser = async (req, res) => {
  try {
    const user = await User.findById(req.user._id);
    res.json({ success: true, user });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// @desc    Logout user
export const logoutUser = (req, res) => {
  res.json({ success: true, message: "Logged out successfully" });
};